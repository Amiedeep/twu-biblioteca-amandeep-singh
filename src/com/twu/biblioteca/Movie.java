public class Movie {
    private String name;
    private String Year;
    private float rating;
    private String director;

    public String getName() {
        return name;
    }

    public String getYear() {
        return Year;
    }

    public String getDirector() {
        return director;
    }

    public float getRating() {
        return rating;
    }
}
